####Week 1 Type of errors

##Discrete Data
##True positive   = correctly identified
##False positive  = incorrectly identified
##True negative   = correctly rejected
##False negative  = incorrectly rejected

##Key quantiles -- example of disease
##Sensitivity               ->  P(positive|disease)
##Specificity               ->  P(negative test|no disease)
##Positive Predictive Value ->  P(disease|positive test)
##Negative Predictive Value ->  P(no disease | negtive test)
##Accuracy                  ->  P(correct outcome)
##Prevalence                ->  Chance of having disease

##example  0.1% prevalence, 99% sensitivity and specificity
##gives 9% positive predictive value due to low prevalence

